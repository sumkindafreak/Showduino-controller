void fx000(Adafruit_NeoPixel& strip, int start, int count, uint32_t color, int speed, int brightness, bool reverse) {
  for (int i = 0; i < count; i++) {
    int index = reverse ? start + count - 1 - i : start + i;
    uint32_t adjustedColor = strip.Color(
      (red(color) * brightness) / 255,
      (green(color) * brightness) / 255,
      (blue(color) * brightness) / 255
    );
    strip.setPixelColor(index, adjustedColor);
    strip.show();
    delay(speed);
  }
}

